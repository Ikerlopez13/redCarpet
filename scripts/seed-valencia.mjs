#!/usr/bin/env node
/**
 * seed:valencia — one-command sandbox bootstrap.
 *
 * 1. Downloads (or reuses) the official district/barrio boundaries from the
 *    Ajuntament de València geoportal (OPENDATA layers 224/225, CC-BY-4.0).
 * 2. Generates supabase/seed/valencia_seed.sql with:
 *      - the `valencia` city (boundary = union of official districts)
 *      - all 88 barrios with official codes and geometry
 *      - the scoring config (config/valencia-scoring.config.json)
 *      - the city_id backfill + coverage verification + initial score compute
 *      - a demo city_admin user (demo.valencia@redcarpet.app)
 * 3. If SUPABASE_DB_URL is set (or `supabase db` is linked), executes it.
 *    Otherwise it tells you to paste the file into the Supabase SQL editor —
 *    the same workflow used by every migration in this repo.
 *
 * Usage:  node scripts/seed-valencia.mjs [--offline] [--no-exec]
 */
import { readFileSync, writeFileSync, existsSync, mkdirSync } from 'node:fs';
import { execSync } from 'node:child_process';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const ROOT = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');
const DATA_DIR = path.join(ROOT, 'data', 'valencia');
const SEED_DIR = path.join(ROOT, 'supabase', 'seed');
const GEOPORTAL = 'https://geoportal.valencia.es/server/rest/services/OPENDATA/UrbanismoEInfraestructuras/MapServer';
const LAYERS = { barrios: 224, distritos: 225 };

const args = new Set(process.argv.slice(2));
const offline = args.has('--offline');
const noExec = args.has('--no-exec');

async function download(layer, file) {
  const dest = path.join(DATA_DIR, file);
  if (offline || existsSync(dest)) {
    console.log(`✓ using cached ${file}`);
    return JSON.parse(readFileSync(dest, 'utf8'));
  }
  const url = `${GEOPORTAL}/${layer}/query?where=1%3D1&outFields=*&outSR=4326&f=geojson`;
  console.log(`↓ downloading layer ${layer} → ${file}`);
  const res = await fetch(url);
  if (!res.ok) throw new Error(`Geoportal returned ${res.status} for layer ${layer}`);
  const geo = await res.json();
  if (!geo.features?.length) throw new Error(`Layer ${layer} returned no features`);
  mkdirSync(DATA_DIR, { recursive: true });
  writeFileSync(dest, JSON.stringify(geo));
  return geo;
}

// Escape a string for a SQL literal
const q = (s) => `'${String(s).replace(/'/g, "''")}'`;
// A geometry literal from a GeoJSON geometry, forced to MultiPolygon 4326
const geomMulti = (g) =>
  `ST_Multi(ST_MakeValid(ST_SetSRID(ST_GeomFromGeoJSON(${q(JSON.stringify(g))}), 4326)))`;

const main = async () => {
  const barrios = await download(LAYERS.barrios, 'barris-barrios.geojson');
  const distritos = await download(LAYERS.distritos, 'districtes-distritos.geojson');
  console.log(`  ${distritos.features.length} districts, ${barrios.features.length} barrios`);

  // district code → name lookup (barrio features only carry the code)
  const districtNames = Object.fromEntries(
    distritos.features.map((f) => [String(f.properties.coddistrit), f.properties.nombre])
  );

  const lines = [];
  lines.push(`-- GENERATED by scripts/seed-valencia.mjs — do not edit by hand.
-- Source: Ajuntament de València geoportal (CC-BY-4.0)
--   districts: ${GEOPORTAL}/${LAYERS.distritos}
--   barrios:   ${GEOPORTAL}/${LAYERS.barrios}
-- Generated: ${new Date().toISOString()}
BEGIN;`);

  // ---- city (boundary = union of official districts) ----
  const districtGeoms = distritos.features.map((f) => geomMulti(f.geometry));
  lines.push(`
INSERT INTO cities (name, slug, country, timezone, active, boundary)
VALUES ('València', 'valencia', 'ES', 'Europe/Madrid', TRUE,
        ST_Multi(ST_MakeValid(ST_Union(ARRAY[${districtGeoms.join(',\n')}]))))
ON CONFLICT (slug) DO UPDATE SET boundary = EXCLUDED.boundary, updated_at = NOW();`);

  // ---- barrios ----
  // the official dataset reuses coddistbar 175 for two Poblats del Nord
  // pedanías (Rafalell-Vistabella / Mahuella-Tauladella) — disambiguate so
  // both survive the (city_id, code) uniqueness
  const seenCodes = new Map();
  for (const f of barrios.features) {
    const p = f.properties;
    const districtCode = String(p.coddistrit);
    let code = String(p.coddistbar);
    const times = seenCodes.get(code) ?? 0;
    seenCodes.set(code, times + 1);
    if (times > 0) code = `${code}-${times + 1}`;
    lines.push(`
INSERT INTO neighborhoods (city_id, district_code, district_name, code, name_es, boundary, centroid)
SELECT c.id, ${q(districtCode)}, ${q(districtNames[districtCode] ?? 'DESCONOCIDO')}, ${q(code)}, ${q(p.nombre)},
       ${geomMulti(f.geometry)},
       ST_PointOnSurface(${geomMulti(f.geometry)})
FROM cities c WHERE c.slug = 'valencia'
ON CONFLICT (city_id, code) DO UPDATE
  SET boundary = EXCLUDED.boundary, centroid = EXCLUDED.centroid,
      name_es = EXCLUDED.name_es, district_name = EXCLUDED.district_name, updated_at = NOW();`);
  }

  // ---- scoring config ----
  const cfg = readFileSync(path.join(ROOT, 'config', 'valencia-scoring.config.json'), 'utf8');
  lines.push(`
INSERT INTO scoring_configs (city_id, config)
SELECT id, ${q(cfg)}::jsonb FROM cities WHERE slug = 'valencia'
ON CONFLICT (city_id) DO UPDATE SET config = EXCLUDED.config, updated_at = NOW();`);

  // ---- backfill, verification, initial scores ----
  lines.push(`
SELECT * FROM backfill_city_ids();
SELECT * FROM verify_neighborhood_coverage('valencia');
SELECT recompute_neighborhood_scores('valencia') AS barrios_scored;`);

  // ---- demo city_admin (password set on first login via Supabase invite) ----
  lines.push(`
-- Demo dashboard admin. auth.users row must exist first; create it in the
-- Supabase Auth panel (or via admin API) with this email, then this insert
-- links it. No-op when the auth user does not exist yet.
INSERT INTO dashboard_users (id, role, city_id, display_name)
SELECT u.id, 'city_admin', c.id, 'Demo Ajuntament de València'
FROM auth.users u, cities c
WHERE u.email = 'demo.valencia@redcarpet.app' AND c.slug = 'valencia'
ON CONFLICT (id) DO UPDATE SET role = 'city_admin', city_id = EXCLUDED.city_id;

COMMIT;`);

  mkdirSync(SEED_DIR, { recursive: true });
  const outFile = path.join(SEED_DIR, 'valencia_seed.sql');
  writeFileSync(outFile, lines.join('\n'));
  console.log(`✓ wrote ${path.relative(ROOT, outFile)} (${(lines.join('\n').length / 1024 / 1024).toFixed(1)} MB)`);

  if (noExec) return;
  const dbUrl = process.env.SUPABASE_DB_URL;
  if (dbUrl) {
    console.log('→ executing against SUPABASE_DB_URL…');
    execSync(`psql "${dbUrl}" -v ON_ERROR_STOP=1 -f "${outFile}"`, { stdio: 'inherit' });
    console.log('✓ seed applied');
  } else {
    console.log(`
Next step: apply the seed. Either
  • set SUPABASE_DB_URL and re-run, or
  • paste supabase/seed/valencia_seed.sql into the Supabase SQL editor
    (run the four 202607031*.sql migrations first).`);
  }
};

main().catch((e) => {
  console.error('✗ seed failed:', e.message);
  process.exit(1);
});
