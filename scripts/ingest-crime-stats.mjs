#!/usr/bin/env node
/**
 * ingest:crime-stats — Layer 1 of the barrio scoring engine.
 *
 * The Ministerio del Interior publishes the Balance de Criminalidad quarterly
 * at municipality level. Automated download is blocked (interior.gob.es sits
 * behind a Cloudflare challenge and the SES portal is a JS application), so
 * the quarterly pull is MANUAL — takes ~5 minutes:
 *
 *   1. Open https://estadisticasdecriminalidad.ses.mir.es/publico/portalestadistico/balances
 *      (or the PDF: interior.gob.es → Prensa → Balances e informes).
 *   2. Find "Municipio de València" in the Balance for the latest quarter.
 *   3. Copy data/valencia/crime-stats-TEMPLATE.json to
 *      data/valencia/crime-stats-<PERIOD>.json (e.g. crime-stats-2026-T1.json)
 *      and fill the real figures.
 *   4. Run: node scripts/ingest-crime-stats.mjs data/valencia/crime-stats-2026-T1.json
 *
 * The script validates the input, writes an idempotent SQL import (period +
 * source + import timestamp stored for auditability) and executes it when
 * SUPABASE_DB_URL is set; otherwise it emits the SQL for the Supabase editor.
 * After a successful import, re-run the scoring:
 *   SELECT recompute_neighborhood_scores('valencia');
 */
import { readFileSync, writeFileSync, mkdirSync } from 'node:fs';
import { execSync } from 'node:child_process';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const ROOT = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');
const input = process.argv[2];
if (!input) {
  console.error('Usage: node scripts/ingest-crime-stats.mjs <data/valencia/crime-stats-YYYY-TN.json>');
  process.exit(1);
}

const REQUIRED = ['city_slug', 'period', 'source', 'source_url', 'population', 'total_infracciones', 'categories'];
const data = JSON.parse(readFileSync(path.resolve(ROOT, input), 'utf8'));

const missing = REQUIRED.filter((k) => data[k] === undefined || data[k] === null || data[k] === '');
if (missing.length) {
  console.error(`✗ missing fields: ${missing.join(', ')}`);
  process.exit(1);
}
if (data._template) {
  console.error('✗ this is the TEMPLATE file — copy it, fill real figures from the Balance, then ingest the copy.');
  process.exit(1);
}
if (!Number.isInteger(data.population) || data.population < 1000) {
  console.error('✗ population must be the official INE municipal population (integer)');
  process.exit(1);
}
if (!Number.isInteger(data.total_infracciones) || data.total_infracciones < 0) {
  console.error('✗ total_infracciones must be a non-negative integer');
  process.exit(1);
}
const catSum = Object.values(data.categories).reduce((a, b) => a + b, 0);
if (catSum > data.total_infracciones) {
  console.error(`✗ category sum (${catSum}) exceeds total_infracciones (${data.total_infracciones})`);
  process.exit(1);
}

const q = (s) => `'${String(s).replace(/'/g, "''")}'`;
const sql = `-- GENERATED by scripts/ingest-crime-stats.mjs — ${new Date().toISOString()}
INSERT INTO crime_stats_municipal
  (city_id, period, source, source_url, population, total_infracciones, categories, imported_by)
SELECT c.id, ${q(data.period)}, ${q(data.source)}, ${q(data.source_url)},
       ${data.population}, ${data.total_infracciones},
       ${q(JSON.stringify(data.categories))}::jsonb,
       ${q(process.env.USER || 'ingest-script')}
FROM cities c WHERE c.slug = ${q(data.city_slug)}
ON CONFLICT (city_id, period, source) DO UPDATE
  SET population = EXCLUDED.population,
      total_infracciones = EXCLUDED.total_infracciones,
      categories = EXCLUDED.categories,
      source_url = EXCLUDED.source_url,
      imported_at = NOW();

SELECT recompute_neighborhood_scores(${q(data.city_slug)}) AS barrios_rescored;
`;

const outDir = path.join(ROOT, 'supabase', 'seed');
mkdirSync(outDir, { recursive: true });
const outFile = path.join(outDir, `crime_stats_${data.city_slug}_${data.period}.sql`);
writeFileSync(outFile, sql);
console.log(`✓ rate: ${(data.total_infracciones / data.population * 1000).toFixed(1)} infracciones/1000 hab`);
console.log(`✓ wrote ${path.relative(ROOT, outFile)}`);

if (process.env.SUPABASE_DB_URL) {
  execSync(`psql "${process.env.SUPABASE_DB_URL}" -v ON_ERROR_STOP=1 -f "${outFile}"`, { stdio: 'inherit' });
  console.log('✓ imported and scores recomputed');
} else {
  console.log('→ SUPABASE_DB_URL not set: paste the file above into the Supabase SQL editor.');
}
